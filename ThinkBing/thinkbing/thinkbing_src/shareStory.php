        <h1>Share Your Story</h1>
        <p>Binghamton University’s bold new strategic plan – Think Green. Think Global. Think Binghamton – has the potential to push Binghamton’s already stellar reputation to new heights and the ability to revitalize the economy of upstate New York. To succeed, however, the University must secure adequate state support as well as a new level of flexibility and autonomy. We need our state leaders to take bold steps to free Binghamton University from state regulations that continue to get in the way of growth and development.</p>
<p>Your story will help us make the case that investment in higher education has a real impact on people’s lives. Please take a few moments to share your story. Tell us what impact Binghamton University has made in your life and why the University is important 
to you. We will share your story – <a title="Shared Stories" href="http://www2.binghamton.edu/think/join-us/shared-stories/index.html">along with those submitted by other University advocates</a> - on the Think Binghamton site and in other materials to demonstrate how the University enriches lives across the region, state and nation.</p>
